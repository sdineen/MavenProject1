package design.patterns.strategy;

public class Square {
	private int x;
	private int y;
	
 	public Square(int i, int j) {
		this.setX(x);
		this.setY(y);
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}

}
